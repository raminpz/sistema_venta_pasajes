-- =========================================================
-- 15. VENTAS
-- =========================================================
CREATE TABLE VENTA (
    ID_VENTA            INT AUTO_INCREMENT PRIMARY KEY,
    ID_USUARIO          INT NOT NULL,
    ID_TIPO_COMPROBANTE INT NOT NULL,
    SERIE               VARCHAR(10) NOT NULL,
    CORRELATIVO         INT NOT NULL,
    FECHA_VENTA         TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    NOTA                VARCHAR(255),
    OBSERVACIONES       TEXT,
    QR_CODE             VARCHAR(255),
    SUBTOTAL            DECIMAL(10,2),
    IGV                 DECIMAL(10,2),
    TOTAL               DECIMAL(10,2) NOT NULL,
    ESTADO              ENUM('REGISTRADA', 'PAGADA', 'ANULADA') NOT NULL DEFAULT 'REGISTRADA',
    CONSTRAINT FK_VENTA_USUARIO
        FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO (ID_USUARIO),
    CONSTRAINT FK_VENTA_TIPO_COMPROBANTE
        FOREIGN KEY (ID_TIPO_COMPROBANTE) REFERENCES TIPO_COMPROBANTE (ID_TIPO),
    CONSTRAINT CHK_VENTA_SUBTOTAL
        CHECK (SUBTOTAL >= 0),
    CONSTRAINT CHK_VENTA_IGV
        CHECK (IGV >= 0),
    CONSTRAINT CHK_VENTA_TOTAL
        CHECK (TOTAL > 0),
    UNIQUE KEY UQ_VENTA_COMPROBANTE (ID_TIPO_COMPROBANTE, SERIE, CORRELATIVO)
) ENGINE = InnoDB;

