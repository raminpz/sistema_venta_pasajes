        FOREIGN KEY (ID_CONDUCTOR) REFERENCES CONDUCTOR (ID_CONDUCTOR),
    CONSTRAINT CHK_VIAJE_PRECIO
        CHECK (PRECIO_BASE > 0),
    CONSTRAINT CHK_VIAJE_FECHAS
        CHECK (FECHA_LLEGADA IS NULL OR FECHA_LLEGADA >= FECHA_SALIDA)
) ENGINE = InnoDB;

