-- =========================================================
-- 18. DETALLE PASAJE
-- =========================================================
CREATE TABLE DETALLE_PASAJE (
    ID_DETALLE     INT AUTO_INCREMENT PRIMARY KEY,
    ID_VENTA       INT NOT NULL,
    ID_VIAJE       INT NOT NULL,
    ID_PASAJERO    INT NOT NULL,
    ID_ASIENTO     INT NOT NULL,
    PRECIO         DECIMAL(10,2) NOT NULL,
    DESCUENTO      DECIMAL(10,2),
    FECHA_REGISTRO TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_DETALLE_VENTA
        FOREIGN KEY (ID_VENTA) REFERENCES VENTA (ID_VENTA),
    CONSTRAINT FK_DETALLE_VIAJE
        FOREIGN KEY (ID_VIAJE) REFERENCES VIAJE (ID_VIAJE),
    CONSTRAINT FK_DETALLE_PASAJERO
        FOREIGN KEY (ID_PASAJERO) REFERENCES PASAJERO (ID_PASAJERO),
    CONSTRAINT FK_DETALLE_ASIENTO
        FOREIGN KEY (ID_ASIENTO) REFERENCES ASIENTO (ID_ASIENTO),
    CONSTRAINT CHK_DETALLE_PRECIO
        CHECK (PRECIO > 0),
    CONSTRAINT CHK_DETALLE_DESCUENTO
        CHECK (DESCUENTO IS NULL OR (DESCUENTO >= 0 AND DESCUENTO <= PRECIO)),
    UNIQUE KEY UQ_VIAJE_ASIENTO (ID_VIAJE, ID_ASIENTO),
    UNIQUE KEY UQ_PASAJERO_VIAJE (ID_PASAJERO, ID_VIAJE)
) ENGINE = InnoDB;

-- =========================================================
-- 19. ENCOMIENDAS
-- =========================================================
CREATE TABLE ENCOMIENDA (
    ID_ENCOMIENDA       INT AUTO_INCREMENT PRIMARY KEY,
    ID_VENTA            INT NOT NULL,
    ID_VIAJE            INT NOT NULL,
    DESCRIPCION         VARCHAR(300),
    PESO_KG             DECIMAL(8,2),
    COSTO               DECIMAL(10,2) NOT NULL,
    REMITENTE_NOMBRE    VARCHAR(100) NOT NULL,
    REMITENTE_DOC       VARCHAR(20),
    DESTINATARIO_NOMBRE VARCHAR(100) NOT NULL,
    DESTINATARIO_DOC    VARCHAR(20),
    DESTINATARIO_TEL    VARCHAR(20),
    ESTADO              ENUM('PENDIENTE', 'EN_TRANSITO', 'ENTREGADO', 'DEVUELTO') NOT NULL DEFAULT 'PENDIENTE',
    FECHA_REGISTRO      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_ENCOMIENDA_VENTA
        FOREIGN KEY (ID_VENTA) REFERENCES VENTA (ID_VENTA),
    CONSTRAINT FK_ENCOMIENDA_VIAJE
        FOREIGN KEY (ID_VIAJE) REFERENCES VIAJE (ID_VIAJE),
    CONSTRAINT CHK_ENCOMIENDA_PESO
        CHECK (PESO_KG IS NULL OR PESO_KG > 0),
    CONSTRAINT CHK_ENCOMIENDA_COSTO
        CHECK (COSTO > 0)
) ENGINE = InnoDB;


