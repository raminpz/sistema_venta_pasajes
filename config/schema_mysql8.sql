-- =========================================================
-- 5. RUTAS
-- =========================================================
CREATE TABLE RUTA (
    ID_RUTA        INT AUTO_INCREMENT PRIMARY KEY,
    ID_ORIGEN_TERMINAL      INT NOT NULL,
    ID_DESTINO_TERMINAL     INT NOT NULL,
    DURACION_HORAS DECIMAL(5,2),
    CONSTRAINT FK_RUTA_ORIGEN_TERMINAL
        FOREIGN KEY (ID_ORIGEN_TERMINAL) REFERENCES TERMINAL (ID_TERMINAL),
    CONSTRAINT FK_RUTA_DESTINO_TERMINAL
        FOREIGN KEY (ID_DESTINO_TERMINAL) REFERENCES TERMINAL (ID_TERMINAL),
    CONSTRAINT CHK_RUTA_TERMINALES
        CHECK (ID_ORIGEN_TERMINAL <> ID_DESTINO_TERMINAL),
    CONSTRAINT CHK_RUTA_DURACION
        CHECK (DURACION_HORAS IS NULL OR DURACION_HORAS > 0),
    UNIQUE KEY UQ_RUTA_ORIGEN_DESTINO_TERMINAL (ID_ORIGEN_TERMINAL, ID_DESTINO_TERMINAL)
) ENGINE = InnoDB;

-- =========================================================
-- 6. TIPOS DE VEHICULO
-- =========================================================
CREATE TABLE TIPO_VEHICULO (
    ID_TIPO_VEHICULO INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE           ENUM('CAMIONETA', 'AUTO', 'COMBI', 'MINI-BUS') NOT NULL,
    DESCRIPCION      VARCHAR(100)
) ENGINE = InnoDB;

-- =========================================================
-- 7. VEHICULOS
-- =========================================================
CREATE TABLE VEHICULO (
    ID_VEHICULO            INT AUTO_INCREMENT PRIMARY KEY,
    ID_TIPO_VEHICULO       INT NOT NULL,
    NRO_PLACA              VARCHAR(10) NOT NULL UNIQUE,
    MARCA                  VARCHAR(50),
    MODELO                 VARCHAR(50),
    ANIO_FABRICACION       YEAR,
    NUMERO_CHASIS          VARCHAR(50) UNIQUE,
    CAPACIDAD              INT NOT NULL,
    NRO_SOAT               VARCHAR(50),
    FECHA_VENC_SOAT        DATE,
    NRO_REVISION_TECNICA   VARCHAR(50),
    FECHA_VENC_REV_TECNICA DATE,
    ESTADO                 ENUM('ACTIVO', 'INACTIVO') NOT NULL DEFAULT 'ACTIVO',
    CONSTRAINT FK_VEHICULO_TIPO
        FOREIGN KEY (ID_TIPO_VEHICULO) REFERENCES TIPO_VEHICULO (ID_TIPO_VEHICULO),
    CONSTRAINT CHK_VEHICULO_CAPACIDAD
        CHECK (CAPACIDAD > 0)
) ENGINE = InnoDB;

-- =========================================================
-- 8. ASIENTOS
-- =========================================================
CREATE TABLE ASIENTO (
    ID_ASIENTO     INT AUTO_INCREMENT PRIMARY KEY,
    ID_VEHICULO    INT NOT NULL,
    NUMERO_ASIENTO VARCHAR(3) NOT NULL,
    CONSTRAINT FK_ASIENTO_VEHICULO
        FOREIGN KEY (ID_VEHICULO) REFERENCES VEHICULO (ID_VEHICULO),
    UNIQUE KEY UQ_ASIENTO_VEHICULO_NUMERO (ID_VEHICULO, NUMERO_ASIENTO)
) ENGINE = InnoDB;

